package Day10;

public class IncrementDecrement {

	public static void main(String[] args) {
		
		int b = 10 ; 
		//b++   POST INCREMENT 
		// increment b value by 1 
		// and tell me the result next time 
		// b showed up 
		//++b  PRE INCREMENT 
		// increment b value right away 
		// and show the result right away
		System.out.println(  ++b  );
		System.out.println(  b  );
		
	}

}
