package Day6;

public class ComparisonOperators {

	public static void main(String[] args) {
		
	// if you have two number to compare them
		
		int a=10, b=10;
		//checking for equality --> ==
		System.out.println("Is a and b equal :" + (a==b));
		
		int a1=10 , b1=11;
		 //checking for inequality --> !=
		System.out.println("Is a1 and b1 not equal :" +(a1!=b1));
		
		
		//checking for greater or equal -->   >=
		System.out.println("Is a1 greater or equal to b1 :" + (a1>=b1));
		
		//checking for greater --->      >
		System.out.println("Is a1 greater than b1 :" + (a1>b1));
		
		//checking for less or equal  ---> <=
		System.out.println("Is a1 less or equal to b1 :" + (a1<=b1));
		
		//checking for less than -->    <
		System.out.println("Is a1 less than b1 :" + (a1<b1));
		

	}

}
