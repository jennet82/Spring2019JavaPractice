package A_MurodilsClasses;

 class Accumulator{
	 int sum;

	public Accumulator(int sum) {
		super();
		this.sum = sum;
	}

	public int getSum() {
		return sum;
	}

	public void setSum(int sum) {
		this.sum = sum;
	}

	@Override
	public String toString() {
		return "Accumulator [sum=" + sum + "]";
	}
	 
	 
	  
}


